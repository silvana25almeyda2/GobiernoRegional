            ResultSet r= cmd.executeQuery();
            int c=1;
            while(r.next()){   
                
                fila[0]=String.valueOf(c)+"º";
                fila[1]=r.getString(1);
                fila[2]=r.getString(2);
                fila[3]=r.getString(3);
                fila[4]=r.getString(4);
                fila[5]=r.getString(5);
                fila[6]=r.getString(6);
                
                m4.addRow(fila);
                c++;
            }
